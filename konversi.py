# int to float
print(float(5))

# float to integer
print(int(5.6))
print(int(-5.6))

# dari-dan-ke string
print(int("25"))
print(str(25))
print(float("25"))
print(str(25.6))

# konversi kumpulan data
print(set([1,2,3]))
print(tuple({5,6,7}))
print(list('hello'))

# konversi ke dictionary
print(dict([[1,2],[3,4]]))

# tuple to dictionary
print(dict([(3,26),(4,44)]))